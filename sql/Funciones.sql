    RETURN VDATOS;
END;


CREATE OR REPLACE FUNCTION FN_CUR_DATOS_RECARGA(
    VID_MAQUINA  IN NUMBER,
    VID_PRODUCTO IN NUMBER
)
RETURN SYS_REFCURSOR
AS
    DATOS SYS_REFCURSOR;
    VSQL  VARCHAR2(800);
BEGIN
    VSQL := 'SELECT M.CODIGO_MAQUINA,
                    P.NOMBRE,
                    P.STOCK_BODEGA,
                    PM.STOCK_ACTUAL,
                    M.CAPACIDAD_MAXIMA,
                    PM.ID_PRODUCTO_MAQUINA
             FROM PRODUCTO_MAQUINA PM
             JOIN PRODUCTO P ON PM.ID_PRODUCTO = P.ID_PRODUCTO
             JOIN MAQUINA  M ON PM.ID_MAQUINA  = M.ID_MAQUINA
             WHERE PM.ID_MAQUINA  = :P1
               AND PM.ID_PRODUCTO = :P2';

    OPEN DATOS FOR VSQL USING VID_MAQUINA, VID_PRODUCTO;
    RETURN DATOS;
END FN_CUR_DATOS_RECARGA;
