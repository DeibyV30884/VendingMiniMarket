-- Funcion para traer un producto por ID al dialog de editar
CREATE OR REPLACE FUNCTION FN_OBTENER_PRODUCTO(VID IN NUMBER)
RETURN SYS_REFCURSOR
AS
    VDATOS SYS_REFCURSOR;
BEGIN
    OPEN VDATOS FOR
        SELECT P.ID_PRODUCTO, P.NOMBRE, C.NOMBRE AS CATEGORIA,
               PR.NOMBRE AS PROVEEDOR, P.PRECIO_COSTO,
               P.PORCENTAJE_GANANCIA, P.PRECIO_VENTA, P.STOCK_BODEGA
        FROM PRODUCTO P
        JOIN CATEGORIA C ON P.ID_CATEGORIA = C.ID_CATEGORIA
        JOIN PROVEEDOR PR ON P.ID_PROVEEDOR = PR.ID_PROVEEDOR
        WHERE P.ID_PRODUCTO = VID;
    RETURN VDATOS;
END;

-- Funcion para Listar pedidos e historial de pedidos
CREATE OR REPLACE FUNCTION FN_LISTAR_PEDIDOS_PENDIENTES
RETURN SYS_REFCURSOR
AS
    VDATOS SYS_REFCURSOR;
BEGIN
    OPEN VDATOS FOR
        SELECT P.ID_PEDIDO,
               PR.NOMBRE AS NOMBRE_PROVEEDOR,
               TO_CHAR(P.FECHA_PEDIDO, 'DD-MM-YYYY') AS FECHA,
               P.TOTAL,
               P.ESTADO
        FROM PEDIDO P
        JOIN PROVEEDOR PR ON P.ID_PROVEEDOR = PR.ID_PROVEEDOR
        WHERE P.ESTADO = 'Pendiente'
        ORDER BY P.FECHA_PEDIDO DESC;
    RETURN VDATOS;
END;

CREATE OR REPLACE FUNCTION FN_LISTAR_HISTORIAL_PEDIDOS
RETURN SYS_REFCURSOR
AS
    VDATOS SYS_REFCURSOR;
BEGIN
    OPEN VDATOS FOR
        SELECT P.ID_PEDIDO,
               PR.NOMBRE AS NOMBRE_PROVEEDOR,
               TO_CHAR(P.FECHA_PEDIDO, 'DD-MM-YYYY') AS FECHA,
               P.TOTAL,
               P.ESTADO
        FROM PEDIDO P
        JOIN PROVEEDOR PR ON P.ID_PROVEEDOR = PR.ID_PROVEEDOR
        WHERE P.ESTADO IN ('Recibido', 'Cancelado')
        ORDER BY P.FECHA_PEDIDO DESC;
    RETURN VDATOS;
END;


-- Función que lee el total real del pedido desde la BD
CREATE OR REPLACE FUNCTION FN_OBTENER_TOTAL_PEDIDO(VID_PEDIDO IN NUMBER)
RETURN NUMBER
AS
    VTOTAL NUMBER;
BEGIN
    SELECT NVL(TOTAL, 0) INTO VTOTAL
    FROM PEDIDO
    WHERE ID_PEDIDO = VID_PEDIDO;
    RETURN VTOTAL;
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RETURN 0;
END;



CREATE OR REPLACE FUNCTION FN_LISTAR_PRODUCTOS_PROVEEDOR(VID_PROVEEDOR IN NUMBER)
RETURN SYS_REFCURSOR
AS
    VDATOS SYS_REFCURSOR;
BEGIN
    OPEN VDATOS FOR
        SELECT P.ID_PRODUCTO, P.NOMBRE, P.PRECIO_VENTA
        FROM PRODUCTO P
