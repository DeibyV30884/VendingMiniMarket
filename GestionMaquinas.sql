-- Gestión de maquinas

-- Creamos Tabla Rol
CREATE TABLE ROL (
    ID_ROL NUMBER PRIMARY KEY,
    NOMBRE VARCHAR2(30) NOT NULL UNIQUE,
    DESCRIPCION VARCHAR2(200)
);

-- 2. Creamos Tabla Usuario
CREATE TABLE USUARIO (
    ID_USUARIO NUMBER PRIMARY KEY,
    NOMBRE VARCHAR2(100) NOT NULL,
    EMAIL VARCHAR2(100) NOT NULL UNIQUE,
    CONTRASENA VARCHAR2(200) NOT NULL,
    ID_ROL NUMBER NOT NULL,
    FECHA_REGISTRO DATE DEFAULT SYSDATE,
    CONSTRAINT FK_USUARIO_ROL FOREIGN KEY (ID_ROL) REFERENCES ROL(ID_ROL)
);

-- 3. Creamos Tabla Categoria
CREATE TABLE CATEGORIA (
    ID_CATEGORIA NUMBER PRIMARY KEY,
    NOMBRE VARCHAR2(50) NOT NULL UNIQUE,
    DESCRIPCION VARCHAR2(200)
);

-- 4. Creamos Tabla Proveedor
CREATE TABLE PROVEEDOR (
    ID_PROVEEDOR NUMBER PRIMARY KEY,
    NOMBRE VARCHAR2(100) NOT NULL,
    CONTACTO VARCHAR2(100),
    TELEFONO VARCHAR2(20),
    EMAIL VARCHAR2(100) UNIQUE,
    DIRECCION VARCHAR2(200)
);

-- 5. Creamos Tabla Producto
CREATE TABLE PRODUCTO (
    ID_PRODUCTO NUMBER PRIMARY KEY,
    NOMBRE VARCHAR2(100) NOT NULL,
    ID_CATEGORIA NUMBER NOT NULL,
    ID_PROVEEDOR NUMBER NOT NULL,
    PRECIO_COSTO NUMBER(10,2) NOT NULL,
    PORCENTAJE_GANANCIA NUMBER(5,2) DEFAULT 30,
    PRECIO_VENTA NUMBER(10,2),
    STOCK_BODEGA NUMBER DEFAULT 0,
    FECHA_INGRESO DATE DEFAULT SYSDATE,
    CONSTRAINT FK_PRODUCTO_CATEGORIA FOREIGN KEY (ID_CATEGORIA) REFERENCES CATEGORIA(ID_CATEGORIA),
    CONSTRAINT FK_PRODUCTO_PROVEEDOR FOREIGN KEY (ID_PROVEEDOR) REFERENCES PROVEEDOR(ID_PROVEEDOR),
    CONSTRAINT CK_PRECIO_COSTO CHECK (PRECIO_COSTO > 0),
    CONSTRAINT CK_PORCENTAJE CHECK (PORCENTAJE_GANANCIA BETWEEN 0 AND 300)
);

-- 6. Creamos Tabla Maquina
CREATE TABLE MAQUINA (
    ID_MAQUINA NUMBER PRIMARY KEY,
    CODIGO_MAQUINA VARCHAR2(50) NOT NULL UNIQUE,
    UBICACION VARCHAR2(200) NOT NULL,
    CAPACIDAD_MAXIMA NUMBER DEFAULT 100,
    ESTADO VARCHAR2(20) NOT NULL,
    FECHA_INSTALACION DATE DEFAULT SYSDATE,
    CONSTRAINT CK_MAQUINA_ESTADO CHECK (ESTADO IN ('ACTIVA', 'INACTIVA', 'MANTENIMIENTO'))
);

-- 7. Creamos Tabla producto_Maquina (relación muchos a muchos)
CREATE TABLE PRODUCTO_MAQUINA (
    ID_PRODUCTO_MAQUINA NUMBER PRIMARY KEY,
    ID_PRODUCTO NUMBER NOT NULL,
    ID_MAQUINA NUMBER NOT NULL,
    STOCK_ACTUAL NUMBER DEFAULT 0,
    STOCK_MINIMO NUMBER DEFAULT 5,
    FECHA_ASIGNACION DATE DEFAULT SYSDATE,
    CONSTRAINT FK_PM_PRODUCTO FOREIGN KEY (ID_PRODUCTO) REFERENCES PRODUCTO(ID_PRODUCTO),
    CONSTRAINT FK_PM_MAQUINA FOREIGN KEY (ID_MAQUINA) REFERENCES MAQUINA(ID_MAQUINA),
    CONSTRAINT UK_PRODUCTO_MAQUINA UNIQUE (ID_PRODUCTO, ID_MAQUINA),
    CONSTRAINT CK_STOCK_MINIMO CHECK (STOCK_MINIMO >= 0)
);

-- 8. Creamos Tabla Pedido
CREATE TABLE PEDIDO (
    ID_PEDIDO NUMBER PRIMARY KEY,
    ID_PROVEEDOR NUMBER NOT NULL,
    ID_USUARIO NUMBER NOT NULL,
    FECHA_PEDIDO DATE DEFAULT SYSDATE,
    FECHA_RECEPCION DATE,
    ESTADO VARCHAR2(20) DEFAULT 'PENDIENTE',
    TOTAL NUMBER(12,2) DEFAULT 0,
    CONSTRAINT FK_PEDIDO_PROVEEDOR FOREIGN KEY (ID_PROVEEDOR) REFERENCES PROVEEDOR(ID_PROVEEDOR),
    CONSTRAINT FK_PEDIDO_USUARIO FOREIGN KEY (ID_USUARIO) REFERENCES USUARIO(ID_USUARIO),
    CONSTRAINT CK_PEDIDO_ESTADO CHECK (ESTADO IN ('PENDIENTE', 'RECIBIDO', 'CANCELADO'))
);

-- 9. Creamos Tabla Detalle_Pedido
CREATE TABLE DETALLE_PEDIDO (
    ID_DETALLE NUMBER PRIMARY KEY,
    ID_PEDIDO NUMBER NOT NULL,
    ID_PRODUCTO NUMBER NOT NULL,
    CANTIDAD NUMBER NOT NULL,
    PRECIO_UNITARIO NUMBER(10,2) NOT NULL,
    SUBTOTAL NUMBER(12,2),
    CONSTRAINT FK_DETALLE_PEDIDO FOREIGN KEY (ID_PEDIDO) REFERENCES PEDIDO(ID_PEDIDO),
    CONSTRAINT FK_DETALLE_PRODUCTO FOREIGN KEY (ID_PRODUCTO) REFERENCES PRODUCTO(ID_PRODUCTO),
    CONSTRAINT CK_CANTIDAD CHECK (CANTIDAD > 0)
);

-- 10. Creamos Tabla MOVIMIENTO_INVENTARIO
CREATE TABLE MOVIMIENTO_INVENTARIO (
    ID_MOVIMIENTO NUMBER PRIMARY KEY,
    ID_PRODUCTO NUMBER NOT NULL,
    ID_USUARIO NUMBER NOT NULL,
    ID_MAQUINA NUMBER,
    TIPO_MOVIMIENTO VARCHAR2(30) NOT NULL,
    CANTIDAD NUMBER NOT NULL,
    FECHA DATE DEFAULT SYSDATE,
    CONSTRAINT FK_MOV_PRODUCTO FOREIGN KEY (ID_PRODUCTO) REFERENCES PRODUCTO(ID_PRODUCTO),
    CONSTRAINT FK_MOV_USUARIO FOREIGN KEY (ID_USUARIO) REFERENCES USUARIO(ID_USUARIO),
    CONSTRAINT FK_MOV_MAQUINA FOREIGN KEY (ID_MAQUINA) REFERENCES MAQUINA(ID_MAQUINA),
    CONSTRAINT CK_TIPO_MOVIMIENTO CHECK (TIPO_MOVIMIENTO IN ('ENTRADA POR PEDIDO', 'RECARGA A MAQUINA', 'VENTA EN MAQUINA', 'AJUSTE MANUAL'))
);

-- 11. Creamos Tabla Venta
CREATE TABLE VENTA (
    ID_VENTA NUMBER PRIMARY KEY,
    ID_PRODUCTO_MAQUINA NUMBER NOT NULL,
    ID_USUARIO NUMBER NOT NULL,
    CANTIDAD NUMBER NOT NULL,
    PRECIO_UNITARIO NUMBER(10,2) NOT NULL,
    TOTAL NUMBER(12,2),
    FECHA_VENTA DATE DEFAULT SYSDATE,
    CONSTRAINT FK_VENTA_PM FOREIGN KEY (ID_PRODUCTO_MAQUINA) REFERENCES PRODUCTO_MAQUINA(ID_PRODUCTO_MAQUINA),
    CONSTRAINT FK_VENTA_USUARIO FOREIGN KEY (ID_USUARIO) REFERENCES USUARIO(ID_USUARIO),
    CONSTRAINT CK_VENTA_CANTIDAD CHECK (CANTIDAD > 0)
);


