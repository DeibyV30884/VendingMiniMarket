CREATE OR REPLACE PROCEDURE SP_LISTAR_MAQUINAS(
    p_cursor OUT SYS_REFCURSOR
)
AS
BEGIN
    OPEN p_cursor FOR
        SELECT ID_MAQUINA,
               CODIGO_MAQUINA,
               UBICACION,
               CAPACIDAD_MAXIMA,
               ESTADO,
               TO_CHAR(FECHA_INSTALACION, 'DD/MM/YYYY') AS FECHA_INSTALACION
        FROM MAQUINA
        ORDER BY ID_MAQUINA;
END SP_LISTAR_MAQUINAS;
/

--Procedure SP_LISTAR_MAQUINAS compilado

SET SERVEROUTPUT ON;

DECLARE
    v_cursor SYS_REFCURSOR;
    v_id NUMBER;
    v_codigo VARCHAR2(50);
    v_ubicacion VARCHAR2(200);
    v_capacidad NUMBER;
    v_estado VARCHAR2(20);
    v_fecha VARCHAR2(20);
BEGIN
    SP_LISTAR_MAQUINAS(v_cursor);
    DBMS_OUTPUT.PUT_LINE('*** LISTADO DE MÁQUINAS ***');
    LOOP
        FETCH v_cursor INTO v_id, v_codigo, v_ubicacion, v_capacidad, v_estado, v_fecha;
        EXIT WHEN v_cursor%NOTFOUND;
        DBMS_OUTPUT.PUT_LINE(v_id || ' | ' || v_codigo || ' | ' || v_ubicacion || ' | ' || v_estado);
    END LOOP;
    CLOSE v_cursor;
END;
/

